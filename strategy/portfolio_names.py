"""포트폴리오 랜덤 이름 생성기

동물, 식물, 색깔 카테고리에서 무작위로 단어를 선택하고
번호를 붙여 재미있는 포트폴리오 이름을 생성합니다.
예: '판다-07', '벚꽃-42', '하늘-13'
"""
import random

_ANIMALS = [
    "고양이", "강아지", "토끼", "판다", "코알라", "여우", "수달", "펭귄",
    "돌고래", "부엉이", "햄스터", "알파카", "사슴", "다람쥐", "해달",
    "치타", "라쿤", "카피바라", "미어캣", "플라밍고",
]

_COLORS = [
    "빨강", "파랑", "초록", "보라", "노랑", "분홍", "하늘", "주황",
    "민트", "코랄", "금빛", "은빛", "무지개", "라벤더", "피치",
    "아쿠아", "로즈", "올리브", "터콰이즈", "루비",
]

_PLANTS = [
    "장미", "해바라기", "벚꽃", "튤립", "라일락", "은행", "단풍", "민들레",
    "수국", "목련", "개나리", "동백", "클로버", "국화", "매화",
    "자스민", "아이리스", "카멜리아", "프리지아", "안개꽃",
]

_ALL_POOLS = [_ANIMALS, _COLORS, _PLANTS]


def generate_name() -> str:
    """랜덤 카테고리에서 단어 + 번호 조합으로 포트폴리오 이름 생성

    Returns:
        예: '판다-07', '벚꽃-42', '하늘-13'
    """
    pool = random.choice(_ALL_POOLS)
    word = random.choice(pool)
    number = random.randint(1, 99)
    return f"{word}-{number:02d}"
